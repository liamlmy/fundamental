class LRUCache {
    class Node {
        int key;
        int val;
        Node prev;
        Node next;

        public Node() {
            this.key = 0;
            this.val = 0;
            prev = null;
            next = null;
        }

        public Node(int key, int val) {
            this.key = key;
            this.val = val;
            prev = null;
            next = null;
        }
    }

    Map<Integer, Node> map;
    int capacity;
    int size;
    Node bottom;
    Node up;

    public LRUCache(int capacity) {
        this.capacity = capacity;
        this.size = 0;
        bottom = new Node();
        up = new Node();
        map = new HashMap<>();
        bottom.next = up;
        up.prev = bottom;
    }
    
    public int get(int key) {
        if (!map.containsKey(key)) {
            return -1;
        }
        moveToUp(key);
        return map.get(key).val;
    }
    
    public void put(int key, int value) {
        if (!map.containsKey(key)) {
            if (this.size == this.capacity) {
                deleteBottomNode();
            }
            addNode(key, value);
        } else {
            map.get(key).val = value;
            moveToUp(key);
        }
    }

    private void deleteBottomNode() {
        this.map.remove(this.bottom.next.key);
        this.bottom.next = this.bottom.next.next;
        this.bottom.next.prev = this.bottom;
        this.size--;
    }

    private void addNode(int key, int val) {
        Node node = new Node(key, val);
        this.map.put(key, node);
        node.prev = this.up.prev;
        node.next = this.up;
        this.up.prev.next = node;
        this.up.prev = node;
        this.size++;
    }

    private void moveToUp(int key) {
        Node node = this.map.get(key);
        node.next.prev = node.prev;
        node.prev.next = node.next;
        node.prev = this.up.prev;
        this.up.prev.next = node;
        node.next = this.up;
        this.up.prev = node;
    }
}

