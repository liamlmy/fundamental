class Solution {
    boolean[][] row;
    boolean[][] col;
    boolean[][][] sub;
    boolean valid;

    public void solveSudoku(char[][] board) {
        row = new boolean[9][9];
        col = new boolean[9][9];
        sub = new boolean[3][3][9];
        List<int[]> index = new ArrayList<>();
        valid = false;
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] == '.') {
                    index.add(new int[] {i, j});
                } else {
                    int cur = board[i][j] - '0' - 1;
                    row[i][cur] = true;
                    col[j][cur] = true;
                    sub[i / 3][j / 3][cur] = true;
                }
            }
        }
        DFS(board, index, 0);
    }

    private void DFS(char[][] board, List<int[]> index, int idx) {
        // base case
        if (idx == index.size()) {
            valid = true;
            return;
        }
        // recursion rule
        int i = index.get(idx)[0];
        int j = index.get(idx)[1];
        for (int k = 1; k <= 9 && !valid; k++) {
            if (!row[i][k - 1] && !col[j][k - 1] && !sub[i / 3][j / 3][k - 1]) {
                row[i][k - 1] = true;
                col[j][k - 1] = true;
                sub[i / 3][j / 3][k - 1] = true;
                board[i][j] = (char) (k + '0');
                DFS(board, index, idx + 1);
                row[i][k - 1] = false;
                col[j][k - 1] = false;
                sub[i / 3][j / 3][k - 1] = false;
            }
        }
    }
}
